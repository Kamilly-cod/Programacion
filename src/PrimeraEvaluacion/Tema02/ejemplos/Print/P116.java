package PrimeraEvaluacion.Tema02.ejemplos.Print;

public class P116 {
    public static void main(String[] args) {
        int i = 0;
        System.out.println(i++); // primero se imprime y luego se incrementa
        System.out.println(i);
        System.out.println(++i); // primero se incrementa y luego se imprima
    }
}
